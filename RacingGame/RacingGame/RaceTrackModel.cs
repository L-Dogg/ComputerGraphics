	public enum CollisionType
	{
		None,
		Boundary
	}

	public class RaceTrackModel : IModel
	{
		private const int FloorTypes = 8;


		private Effect _effect;
		private int[,] _floorPlan;
		private GraphicsDevice _device;
		private BoundingBox _raceTrackBox;
		private Texture2D _sceneryTexture;
		private VertexBuffer _vertexBuffer;

		public Vector3 Position { get; set; }

		public RaceTrackModel(Effect effect, Texture2D texture, GraphicsDevice device)
		{
			_effect = effect;
			_sceneryTexture = texture;

			_device = device;

			LoadFloorPlan();
			SetUpVertices();
			SetUpBoundingBoxes();
		}

		public void Update()
		{
			return;
		}

		public void Draw(Matrix _viewMatrix, Matrix _projectionMatrix, Vector3 _carPosition)
		{
			_effect.Parameters["xUseColors"].SetValue(false);
			_effect.CurrentTechnique = _effect.Techniques[Lights.CurrentModel];
			_effect.Parameters["xWorld"].SetValue(Matrix.Identity);
			_effect.Parameters["xView"].SetValue(_viewMatrix);
			_effect.Parameters["xProjection"].SetValue(_projectionMatrix);
			_effect.Parameters["xTexture"].SetValue(_sceneryTexture);

			_effect.Parameters["Ka"].SetValue(0.5f);
			_effect.Parameters["Ks"].SetValue(0.5f);
			_effect.Parameters["Kd"].SetValue(0.75f);
			_effect.Parameters["A"].SetValue(15f);

			_effect.Parameters["xCamUp"].SetValue(Cameras.CameraUp);
			_effect.Parameters["xCamPos"].SetValue(Cameras.CameraPosition);
			_effect.Parameters["xLightPositions"].SetValue(Lights.LightPositions);
			_effect.Parameters["xLightColors"].SetValue(Lights.LightColors);
			_effect.Parameters["xLightCount"].SetValue(Lights.LightsCount);
			_effect.Parameters["xCarLightPositions"].SetValue(Lights.CarLightPositions);
			_effect.Parameters["xCarLightColors"].SetValue(Lights.CarLightColors);

			foreach (var pass in _effect.CurrentTechnique.Passes)
			{
				pass.Apply();
				_device.SetVertexBuffer(_vertexBuffer);
				_device.DrawPrimitives(PrimitiveType.TriangleList, 0, _vertexBuffer.VertexCount / 3);
			}
		}
		
		public CollisionType CheckCollision(BoundingSphere sphere)
		{
			return _raceTrackBox.Contains(sphere) != ContainmentType.Contains ? CollisionType.Boundary : CollisionType.None;
		}

